package christmas.model.constants;

public enum MenuType {
    DEFAULT("Default"),
    APPETIZER("Appetizer"),
    MAIN_MENU("MainMenu"),
    DRINK("Drink"),
    DESSERT("Dessert");

    private final String menuType;

    MenuType(String menuType) {
        this.menuType = menuType;
    }

    public String getMenuType() {
        return this.getMenuType();
    }
}
